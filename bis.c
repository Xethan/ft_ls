char	*sort_files(char **arg)
{
	int		i;
	int		i_min;

	i_min = 0;
	while (arg[i_min])
	{
		i = i_min + 1;
		while (arg[i])
		{
			if (ft_strcmp(arg[i], arg[i_min]) < 0)
			i++;
		}
		i_min++;
	}

}